function util.ColorCopy(source, dest, copyAlpha)
	dest.r = source.r
	dest.g = source.g
	dest.b = source.b
	if copyAlpha then dest.a = source.a end
end

function util.CopyColor(col) return Color(col.r, col.g, col.b, col.a) end

function util.ColorModulate(col) return col.r, col.g, col.b end

function util.ModulateColor(r, g, b) return Color(r, g, b) end
